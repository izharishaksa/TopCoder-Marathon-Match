class Letter implements Comparable<Letter> {

    char id;
    double similarity = 0;
    int row;
    int col;
    int bp;

    public Letter(char id, int row, int col, double similarity, int bp) {
        this.id = id;
        this.row = row;
        this.col = col;
        this.similarity = similarity;
        this.bp = bp;
    }

    public int compareTo(Letter o) {
        if (this.similarity > o.similarity) {
            return -1;
        }
        return 1;
    }
}

